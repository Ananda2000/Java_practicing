package basic_Java_Programs;

public class zzz_pojo_student 
{
	private int id;
	private String name;
	
	public zzz_pojo_student(int id, String name)
	{
		this.id = id;
		this.name = name;
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
